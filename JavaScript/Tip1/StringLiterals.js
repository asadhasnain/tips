const firstName = "Asad", lastName = "Bukhari";

/* Variable Substitution */
console.log(`Hi ${firstName}, ${lastName}`); // Hi Asad, Bukhari

/* Expression Substitution */
console.log(`Hi ${firstName.toLowerCase()}!`); // Hi asad!
const a = 2, b = 3;
console.log(`Comparison ${a > b ? true : false}`); // Comparison: false
console.log(`Sum ${2+3}`); // Sum: 5

/* To print multiple lines like: 
Line 1
Line 2
*/
console.log(`Line 1
Line 2`);


